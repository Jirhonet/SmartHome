namespace SmartHome.Enums
{
    public enum SpeakerState
    {
        /// <summary>
        /// 1 - The speaker is off.
        /// </summary>
        Off = 1,

        /// <summary>
        /// 2 - The speaker is on.
        /// </summary>
        On = 2,

        /// <summary>
        /// 3 - The speaker is playing.
        /// </summary>
        Playing = 3,

        /// <summary>
        /// 4 - The speaker is paused.
        /// </summary>
        Paused = 4,
    }
}
